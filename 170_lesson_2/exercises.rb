# 1. What are the required components of an HTTP request?
#    What are the additional optional components?
# Http method are path are required, but parameters, header, and body are not.

# 2. What are the required components of an HTTP response? 
#    What are the additional optional components?
# Status code is required, but header and body are not.

# 3. What determines whether a request should use GET or POST as its HTTP method?
# GET is used to fetch data from a server, but POST is used to push/change data to server.

# 4. Is the host component of a URL included as part of an HTTP request?
# No, just the path and the parameters (if necessary).